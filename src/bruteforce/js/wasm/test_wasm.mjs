const buf = Module._malloc(128);

function testExpansion(label, key, data, expected) {
    return runTest(label, () => {
        Module.ccall('tm_wasm_init', null, ['number'], [key]);
        Module.ccall('tm_wasm_test_expansion', null, ['number','number'], [data, buf]);
        const result = Module.HEAPU8.slice(buf, buf + 128);
        const ok = expected.every((b, i) => b === result[i]);
        if (!ok) {
            const i = expected.findIndex((b, i) => b !== result[i]);
            console.log(`  byte ${i}: expected 0x${expected[i].toString(16)} got 0x${result[i].toString(16)}`);
        }
        return ok;
    });
}

function testPipeline(label, key, data, expected) {
    return runTest(label, () => {
        Module.ccall('tm_wasm_init', null, ['number'], [key]);
        Module.ccall('tm_wasm_test_bruteforce_data', null, ['number','number'], [data, buf]);
        const result = Module.HEAPU8.slice(buf, buf + 128);
        const ok = expected.every((b, i) => b === result[i]);
        if (!ok) {
            const i = expected.findIndex((b, i) => b !== result[i]);
            console.log(`  byte ${i}: expected 0x${expected[i].toString(16)} got 0x${result[i].toString(16)}`);
        }
        return ok;
    });
}

function testChecksum(label, key, data, world, expectValid) {
    return runTest(label, () => {
        Module.ccall('tm_wasm_init', null, ['number'], [key]);
        const r = Module.ccall('tm_wasm_test_bruteforce_checksum', 'number', ['number','number'], [data, world]);
        return expectValid ? r === 1 : r === 0;
    });
}

function testRun(label, key, data) {
    return runTest(label, () => {
        Module.ccall('tm_wasm_init', null, ['number'], [key]);
        const rbuf = Module._malloc(1024);
        const bytes = Module.ccall('tm_wasm_run', 'number',
            ['number','number','number','number'], [data, 1, rbuf, 1024]);
        const found = bytes >= 5 &&
            ((Module.HEAPU8[rbuf] | Module.HEAPU8[rbuf+1]<<8 |
              Module.HEAPU8[rbuf+2]<<16 | Module.HEAPU8[rbuf+3]<<24) >>> 0) === (data >>> 0);
        Module._free(rbuf);
        return found;
    });
}

