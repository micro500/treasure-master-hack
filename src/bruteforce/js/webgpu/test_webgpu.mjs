// Node.js WebGPU shim — the `webgpu` npm package exposes a GPU object
// but does not inject navigator.gpu.  In the browser this block is skipped.
if (typeof navigator === 'undefined' || !navigator.gpu) {
    const { create, globals } = await import('webgpu');
    // Inject GPUBufferUsage, GPUMapMode, etc. into globalThis before any
    // WebGPU calls so tm_seq.mjs can reference them at module level.
    Object.assign(globalThis, globals);
    Object.defineProperty(globalThis, 'navigator', { value: { gpu: create([]) }, configurable: true });
}

const dir = fileURLToPath(new URL('.', import.meta.url));
const shaderSource = await readFile(`${dir}tm_seq.wgsl`, 'utf8');
const tm = await TmWebGPU.create(shaderSource);

function runTest(name, ok) {
    console.log(`${ok ? 'PASS' : 'FAIL'} ${name}`);
    return ok;
}

function bytesEqual(a, b) {
    if (a.length !== b.length) return false;
    for (let i = 0; i < a.length; i++) {
        if (a[i] !== b[i]) {
            console.log(`  byte ${i}: expected 0x${b[i].toString(16)} got 0x${a[i].toString(16)}`);
            return false;
        }
    }
    return true;
}

