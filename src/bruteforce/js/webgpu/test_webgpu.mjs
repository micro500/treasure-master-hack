results.push(runTest('pipeline_v0',
    bytesEqual(await tm.testPipeline(0x0009BE9F), pipeline_v0_expected)));

tm.init(0x2CA5B42D);
results.push(runTest('checksum carnival valid',
    await tm.testChecksum(0xF73A2612, 0) === true));

tm.init(0x12345678);
results.push(runTest('checksum invalid',
    await tm.testChecksum(0x9ABCDEF0, 0) === false));

{
    tm.init(0x2CA5B42D);
    const outBuf = new Uint8Array(1024);
    const bytes  = await tm.run(0xF73A2612, 1, outBuf, outBuf.length);
    const data   = new DataView(outBuf.buffer).getUint32(0, true);
    const found  = bytes >= 5 && (data >>> 0) === (0xF73A2612 >>> 0);
    results.push(runTest('tm_run hit', found));
}

tm.destroy();

const pass = results.filter(Boolean).length;
console.log(`\n${pass}/${results.length} passed`);
process.exit(pass < results.length ? 1 : 0);
